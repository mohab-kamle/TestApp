    private void deleteButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteButtonActionPerformed
        // TODO add your handling code here:
        categoryError.setText("");
        deleteCategoryMsg.setText("");
        if(categoryList.getSelectedIndex() == 0){
            categoryError.setText("No categroy was selected");
        } else {
            CDB.deleteCategory(CDB.getCategoriesList().get(categoryList.getSelectedIndex()-1).getCategoryId());
            deleteCategoryMsg.setText("Category has been deleted !!");
            categoryList.removeItemAt(categoryList.getSelectedIndex());
            categoryList.setSelectedIndex(0);
        }
    }//GEN-LAST:event_deleteButtonActionPerformed

